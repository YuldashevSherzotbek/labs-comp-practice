import cmath

z1 = complex(-1, 1)
z2 = complex(-4, -2)
z3 = complex(-1, -1)

# Сумма
sum_result = z1 + z2
print(f"Сумма: {sum_result}")

# Разность
diff_result = z1 - z2
print(f"Разность: {diff_result}")

# Произведение
prod_result = z1 * z2
print(f"Произведение: {prod_result}")

# Частное
quot_result = z1 / z2
print(f"Частное: {quot_result}")

# Четвертая степень
z3_pow4 = z3 ** 4
print(f"z3 в четвертой степени: {z3_pow4}")

# Корень третьей степени
z3_root3 = z3 ** (1/3)
print(f"Корень третьей степени z3: {z3_root3}")
